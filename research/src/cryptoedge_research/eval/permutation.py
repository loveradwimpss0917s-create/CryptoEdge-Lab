"""Block-permutation significance test (docs/05 §3.5).

Tests whether a signal's mean forward return is distinguishable from
chance timing, without assuming any return distribution — appropriate
for crypto's fat tails (docs/05 §3.5: "分布仮定なしで...t検定より crypto の
ファットテールに頑健").

The null hypothesis is generated by reshuffling *when* the signal fires
(in contiguous blocks, to preserve the fired-return series' own
autocorrelation structure) while holding the underlying forward-return
series fixed, then recomputing what the mean return would have been.
"""

from __future__ import annotations

from dataclasses import dataclass

import numpy as np


@dataclass(frozen=True)
class PermutationResult:
    observed_ev_bps: float
    p_value: float
    n_iterations: int
    null_mean: float
    null_std: float


def _block_shuffle(mask: np.ndarray, block_len: int, rng: np.random.Generator) -> np.ndarray:
    n = len(mask)
    n_blocks = -(-n // block_len)  # ceil
    padded = np.zeros(n_blocks * block_len, dtype=mask.dtype)
    padded[:n] = mask
    blocks = padded.reshape(n_blocks, block_len)
    order = rng.permutation(n_blocks)
    shuffled = blocks[order].reshape(-1)
    return shuffled[:n]


def permutation_test(
    forward_returns_bps: np.ndarray,
    fires: np.ndarray,
    horizon_bars: int,
    n_iterations: int = 1000,
    seed: int = 0,
) -> PermutationResult:
    """`forward_returns_bps[i]` is the return a trade would realize if it
    opened at bar i; `fires[i]` is whether the signal actually fired
    there. Block length is `horizon_bars * 3` per docs/05 §3.5.
    """
    fires = np.asarray(fires, dtype=bool)
    forward_returns_bps = np.asarray(forward_returns_bps, dtype=float)
    if fires.sum() == 0:
        return PermutationResult(0.0, 1.0, n_iterations, 0.0, 0.0)

    observed_ev = float(forward_returns_bps[fires].mean())
    block_len = max(1, horizon_bars * 3)
    rng = np.random.default_rng(seed)

    null_evs = np.empty(n_iterations)
    for i in range(n_iterations):
        shuffled_fires = _block_shuffle(fires, block_len, rng)
        selected = forward_returns_bps[shuffled_fires]
        null_evs[i] = selected.mean() if len(selected) > 0 else 0.0

    p_value = float(np.mean(null_evs >= observed_ev))
    return PermutationResult(
        observed_ev_bps=observed_ev,
        p_value=p_value,
        n_iterations=n_iterations,
        null_mean=float(np.mean(null_evs)),
        null_std=float(np.std(null_evs, ddof=1)) if n_iterations > 1 else 0.0,
    )
