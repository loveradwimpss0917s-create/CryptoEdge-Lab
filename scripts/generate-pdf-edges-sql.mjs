#!/usr/bin/env node
// Generates seeds/0001_pdf_edges.sql from scripts/pdf-edges-data.mjs.
// Re-run this whenever pdf-edges-data.mjs changes; the SQL file is
// checked in (docs/00 §3 principle 1: reproducibility) so `wrangler d1
// execute --file` doesn't need Node at deploy time.

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { ulid } from "ulid";
import { PDF_EDGES } from "./pdf-edges-data.mjs";

const OUT_PATH = fileURLToPath(new URL("../seeds/0001_pdf_edges.sql", import.meta.url));
const SEED_TIME = Date.parse("2026-07-02T00:00:00.000Z");

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function slugify(title) {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

const lines = [
  "-- Generated by scripts/generate-pdf-edges-sql.mjs — do not hand-edit.",
  "-- Source: scripts/pdf-edges-data.mjs (docs/00 §2, docs/09 §3).",
  "-- Not a schema migration (migrations/ is forward-only DDL, docs/02 header);",
  "-- this is mutable research data, applied once via:",
  "--   wrangler d1 execute cryptoedge --local --file seeds/0001_pdf_edges.sql",
  "--   wrangler d1 execute cryptoedge --remote --file seeds/0001_pdf_edges.sql",
  ""
];

for (const edge of PDF_EDGES) {
  const edgeId = ulid();
  const slug = slugify(edge.title);
  const evidence = JSON.stringify(edge.evidence);
  const priors = JSON.stringify(edge.priors);

  lines.push(
    `INSERT INTO edges (edge_id, slug, title, category, status, hypothesis, rationale, counter_evidence, evidence, origin, pdf_ref, priors, created_at, updated_at)`,
    `VALUES (${sqlString(edgeId)}, ${sqlString(slug)}, ${sqlString(edge.title)}, ${sqlString(edge.category)}, 'IDEA', ${sqlString(edge.hypothesis)}, ${sqlString(edge.rationale)}, ${sqlString(edge.counterEvidence)}, ${sqlString(evidence)}, 'pdf_seed', ${sqlString("EC-" + edge.ref)}, ${sqlString(priors)}, ${SEED_TIME}, ${SEED_TIME});`
  );

  if (edge.version) {
    const versionId = ulid();
    const instrumentId =
      edge.version.direction === "vol" ? "BTC-PERP.DERIBIT.OPT_INDEX" : "BTCUSDT.BINANCE.PERP";
    const signalSpec = JSON.stringify({
      when: edge.version.when,
      entry: { delay_bars: 1, price: "open" },
      exit: { horizon: edge.version.horizon },
      direction: edge.version.direction
    });
    const costModel = JSON.stringify({ taker_bps: 4, slippage_bps: 2, funding_included: false });

    lines.push(
      `INSERT INTO edge_versions (version_id, edge_id, semver, signal_spec, params, instrument_id, direction, horizon, cost_model, changelog, created_at, is_current)`,
      `VALUES (${sqlString(versionId)}, ${sqlString(edgeId)}, ${sqlString(edge.version.semver)}, ${sqlString(signalSpec)}, '{}', ${sqlString(instrumentId)}, ${sqlString(edge.version.direction)}, ${sqlString(edge.version.horizon)}, ${sqlString(costModel)}, 'Initial PDF-seed version (docs/09 §3)', ${SEED_TIME}, 1);`
    );
  }
  lines.push("");
}

writeFileSync(OUT_PATH, lines.join("\n"));
console.log(`wrote ${PDF_EDGES.length} edges (${PDF_EDGES.filter((e) => e.version).length} with a v1 version) to ${OUT_PATH}`);
